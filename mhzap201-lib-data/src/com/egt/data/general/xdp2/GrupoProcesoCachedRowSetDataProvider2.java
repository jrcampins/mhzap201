public class GrupoProcesoCachedRowSetDataProvider2 extends GrupoProcesoCachedRowSetDataProvider
        implements RecursoConsultableDataProvider {

    public GrupoProcesoCachedRowSetDataProvider2() {
        super();
    }

    public GrupoProcesoCachedRowSetDataProvider2(CachedRowSet cachedRowSet) {
        super(cachedRowSet);
        if (cachedRowSet != null) {
            this.initCachedRowSet();
        }
    }

    @Override
    public void setCachedRowSet(CachedRowSet cachedRowSet) {
        super.setCachedRowSet(cachedRowSet);
        if (cachedRowSet != null) {
            this.initCachedRowSet();
        }
    }

    private void initCachedRowSet() {
        try {
            if (!this.isInicializado()) {
                this.setInicializado(true);
                this.setComandoCachedRowSet(this.getComandoSelectNothing());
                this.setTablaCachedRowSet(this.getTablaDestino());
                this.setMapaTiposJava();
                this.setColumnasInsertables();
                this.setColumnasModificables();
            }
        } catch (Exception ex) {
            TLC.getBitacora().error(ex);
        }
    }

    @Override
    public void setComandoEmptyCachedRowSet() throws ExcepcionAplicacion {
        this.setComandoEmptyCachedRowSet(this.getComandoSelectNothing());
    }

    public static final long FUNCION_CONSULTAR_RECURSO = 107500101L;

    public static final long FUNCION_CANCELAR_GRUPO_PROCESO = 107500151L;
    public static final long FUNCION_CONSULTAR_GRUPO_PROCESO = 107500101L;

    public static final String PROCESO_FUNCION_CANCELAR_GRUPO_PROCESO = "grupo_proceso_cancelar";

    protected static final long PARAMETRO_ID_GRUPO_PROCESO = 1333L;
    protected static final long PARAMETRO_VERSION_GRUPO_PROCESO = 1618L;
    protected static final long PARAMETRO_CODIGO_GRUPO_PROCESO = 1051L;
    protected static final long PARAMETRO_NOMBRE_GRUPO_PROCESO = 1424L;
    protected static final long PARAMETRO_DESCRIPCION_GRUPO_PROCESO = 1238L;
    protected static final long PARAMETRO_ID_RASTRO_PROCESO = 1361L;
    protected static final long PARAMETRO_NUMERO_CONDICION_EJE_FUN = 1473L;

    public static final long FUNCION_REFERENCIA_CONSULTAR_GRUPO_PROCESO = 107500101L;
    public static final long FUNCION_REFERENCIA_CONSULTAR_CONDICION_EJE_FUN = 107800101L;

    private static final String[] STRINGS_REFERENCIA_CONSULTAR_GRUPO_PROCESO = {"grupo_proceso", "id_grupo_proceso", "", ""};
    private static final String[] STRINGS_REFERENCIA_CONSULTAR_CONDICION_EJE_FUN = {"condicion_eje_fun", "numero_condicion_eje_fun", "", ""};

    @Override
    public long getFuncionConsultarRecurso() {
        return FUNCION_CONSULTAR_RECURSO;
    }

    @Override
    public void ejecutarFuncionSelect() throws ExcepcionAplicacion {
        if (this.isFuncionSelectAutorizada()) {
            this.ejecutarFuncionSelect(this.getFuncionSelect());
        } else {
            this.ejecutarFuncionSelect(this.getFuncionConsultarRecurso());
        }
    }

    @Override
    public void ejecutarFuncionSelect(long funcion) throws ExcepcionAplicacion {
        this.ejecutarFuncionSelect(funcion, new FiltroBusqueda());
    }

    @Override
    public void ejecutarFuncionSelect(Long filtro) throws ExcepcionAplicacion {
        if (this.isFuncionSelectAutorizada()) {
            this.ejecutarFuncionSelect(this.getFuncionSelect(), filtro);
        } else {
            this.ejecutarFuncionSelect(this.getFuncionConsultarRecurso(), filtro);
        }
    }

    @Override
    public void ejecutarFuncionSelect(long funcion, Long filtro) throws ExcepcionAplicacion {
        if (filtro == null) {
            this.ejecutarFuncionSelect(funcion, new FiltroBusqueda());
        } else {
            this.ejecutarFuncionSelect(funcion, new FiltroBusqueda(filtro));
        }
    }

    @Override
    public void ejecutarFuncionSelect(FiltroBusqueda filtro) throws ExcepcionAplicacion {
        if (this.isFuncionSelectAutorizada()) {
            this.ejecutarFuncionSelect(this.getFuncionSelect(), filtro);
        } else {
            this.ejecutarFuncionSelect(this.getFuncionConsultarRecurso(), filtro);
        }
    }

    @Override
    public void ejecutarFuncionSelect(long funcion, FiltroBusqueda filtro) throws ExcepcionAplicacion {
        if (funcion == FUNCION_CONSULTAR_GRUPO_PROCESO) {
            this.consultarGrupoProceso(filtro);
            return;
        }
    }

    public void consultarGrupoProceso() throws ExcepcionAplicacion {
        this.consultarGrupoProceso(new FiltroBusqueda());
    }

    public void consultarGrupoProceso(Long filtro) throws ExcepcionAplicacion {
        if (filtro == null) {
            this.consultarGrupoProceso(new FiltroBusqueda());
        } else {
            this.consultarGrupoProceso(new FiltroBusqueda(filtro));
        }
    }

    public void consultarGrupoProceso(FiltroBusqueda filtro) throws ExcepcionAplicacion {
        String comando = this.getComandoSelect();
        String clausula = "WHERE";
        String criterios = null;
        try {
            this.setFuncionSelect(FUNCION_CONSULTAR_GRUPO_PROCESO);
            criterios = this.getCriteriosBusqueda();
            if (criterios != null && !criterios.equals("")) {
                comando += clausula + " (" + criterios + ") ";
                clausula = "AND";
            }
            criterios = filtro == null ? null : filtro.toString(CODIGO_DOMINIO_RECURSO);
            if (criterios != null && !criterios.equals("")) {
                comando += clausula + " (" + criterios + ") ";
            }
            clausula = "ORDER BY";
            criterios = this.getCriteriosOrden();
            if (criterios != null && !criterios.equals("")) {
                comando += clausula + " " + criterios + " ";
            } else {
                comando += clausula + " id_grupo_proceso ";
            }
        } catch (ExcepcionAplicacion ex) {
            comando = this.getComandoSelectNothing();
            throw ex;
        } finally {
            this.setComandoCachedRowSet(comando, true);
        }
    }
}
