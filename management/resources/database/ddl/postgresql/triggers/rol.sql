drop trigger if exists rol_before_update_trigger on rol cascade;
-- ----------------------------------------------------------------------------------------------------------------------------------------------------------
create trigger rol_before_update_trigger
before update on rol
for each row execute procedure rol_before_update();
-- ----------------------------------------------------------------------------------------------------------------------------------------------------------
drop trigger if exists rol_before_delete_trigger on rol cascade;
-- ----------------------------------------------------------------------------------------------------------------------------------------------------------
create trigger rol_before_delete_trigger
before delete on rol
for each row execute procedure rol_before_delete();
