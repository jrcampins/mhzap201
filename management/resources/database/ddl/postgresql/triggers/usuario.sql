drop trigger if exists usuario_before_insert_trigger on usuario cascade;
-- ----------------------------------------------------------------------------------------------------------------------------------------------------------
create trigger usuario_before_insert_trigger
before insert on usuario
for each row execute procedure usuario_before_insert();
-- ----------------------------------------------------------------------------------------------------------------------------------------------------------
drop trigger if exists usuario_before_update_trigger on usuario cascade;
-- ----------------------------------------------------------------------------------------------------------------------------------------------------------
create trigger usuario_before_update_trigger
before update on usuario
for each row execute procedure usuario_before_update();
